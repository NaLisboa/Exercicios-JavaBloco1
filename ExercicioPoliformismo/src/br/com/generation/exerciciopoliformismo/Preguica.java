package br.com.generation.exerciciopoliformismo;

public class Preguica implements Animal{

	@Override
	public void nome() {
		System.out.println("Nome: Laurinha");
		
	}

	@Override
	public void idade() {
		System.out.println("Idade: 9 anos");
		
	}

	@Override
	public void emitirSom() {
		System.out.println("Som da preguiça: IHUAHSUAHSKA");
		
	}

	@Override
	public void correr() {
		System.out.println("Bicho preguiça correndo....");
		
	}

}
